// showMusicStat
function showMusicStat(lable) {
  document.querySelector('#schRst').innerHTML += "<br>" + lable + ": " +
    Object.keys(music[0])
      .map(key => `${key} ${music[0][key].length}`)
      .join(' ');
}

function selectRandommusicElements() {
  // Check if music exists and has data
  if (!music || music.length === 0) {
    console.error('music is empty or not defined');
    return null;
  }

  const photoData = music[0];
  const selectedElements = {};

  // Randomly select one element from each array
  for (const [key, array] of Object.entries(photoData)) {
    if (Array.isArray(array) && array.length > 0) {
      const randomIndex = Math.floor(Math.random() * array.length);

      selectedElements[key] = {
        index: randomIndex,
        value: array[randomIndex],
        totalCount: array.length
      };
    }
  }

  return selectedElements;
}

// Function to display the selected elements
function displaymusicRandomSelection() {
  const selection = selectRandommusicElements();

  if (!selection) return;

  // Create a single continuous string without line breaks
  let displayString = '';

  for (const [key, data] of Object.entries(selection)) {
    displayString += `${data.value} `;
  }

  // Update the HTML element
  const questionDiv = document.querySelector('#question');
  if (questionDiv) {
    questionDiv.innerHTML = displayString;
  } else {
    console.error('Element with id "question" not found');
  }

  window.location = '#question';
  window.scrollTo(0,window.scrollY - 40);

  return selection;
}



// Call the function to display random selection
displaymusicRandomSelection();
